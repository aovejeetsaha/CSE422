def astar(n, m, start, goal, maze):
    directions = [(0, 1, 'R'), (1, 0, 'D'), (0, -1, 'L'), (-1, 0, 'U')]
    visited = set()
    heap = [(manhattan(start, goal), 0, start[0], start[1], '')]

def manhattan(u1, u2):
    return abs(u1[0]-u2[0]) + abs(u1[1]-u2[1])

    while heap:
        est_total, costing, x, y, path = heapq.heappop(heap)
        if (x, y) == goal:
            return costing, path
        if (x, y) in visited:
            continue
        visited.add((x, y))
        for dx, dy, move in directions:
            nx, ny = x + dx, y + dy
            if 0 <= nx < n and 0 <= ny < m and maze[nx][ny] == '0' and (nx, ny) not in visited:
                new_costing = costing + 1
                est = new_costing + manhattan((nx, ny), goal)
                heapq.heappush(heap, (est, new_costing, nx, ny, path + move))
    return -1, ''

